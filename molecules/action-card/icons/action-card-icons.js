/**
 * Icon utility functions
 */
export const iconUtils = {
  /**
   * Get icon by name
   */
  getIcon(name) {
    return actionCardIcons[name] || '';
  },

  /**
   * Get icon for status
   */
  getStatusIcon(status) {
    switch (status) {
    case 'completed':
      return actionCardIcons.completed;
    case 'locked':
      return actionCardIcons.locked;
    case 'warning':
      return actionCardIcons.warning;
    default:
      return '';
    }
  },

  /**
   * Get reward icon
   */
  getRewardIcon(type = 'star') {
    const rewardIcons = {
      star: actionCardIcons.star,
      coin: actionCardIcons.coin,
      trophy: actionCardIcons.trophy
    };

    return rewardIcons[type] || actionCardIcons.star;
  },

  /**
   * Create icon element
   */
  createIconElement(iconName, className = '') {
    const icon = this.getIcon(iconName);
    if (!icon) return null;

    const div = document.createElement('div');
    div.className = className;
    div.innerHTML = icon;
    div.setAttribute('aria-hidden', 'true');

    return div;
  }
};
