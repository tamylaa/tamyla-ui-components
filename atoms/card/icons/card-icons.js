// Icon utility functions
export const cardIconUtils = {
  /**
   * Create an icon element with proper styling
   */
  create(iconName, options = {}) {
    const {
      size = '1.25rem',
      className = '',
      color = 'currentColor'
    } = options;

    const iconSVG = cardIcons[iconName];
    if (!iconSVG) {
      console.warn(`Icon "${iconName}" not found in cardIcons`);
      return null;
    }

    const wrapper = document.createElement('span');
    wrapper.className = `tmyl-card__icon ${className}`;
    wrapper.style.cssText = `
      width: ${size};
      height: ${size};
      color: ${color};
      display: inline-flex;
      align-items: center;
      justify-content: center;
      flex-shrink: 0;
    `;
    wrapper.innerHTML = iconSVG;

    // Set SVG attributes
    const svg = wrapper.querySelector('svg');
    if (svg) {
      svg.setAttribute('aria-hidden', 'true');
      svg.setAttribute('focusable', 'false');
      svg.style.width = '100%';
      svg.style.height = '100%';
    }

    return wrapper;
  },

  /**
   * Get available icon names
   */
  getAvailable() {
    return Object.keys(cardIcons);
  },

  /**
   * Get icon SVG string
   */
  getSVG(iconName) {
    return cardIcons[iconName] || null;
  },

  /**
   * Create star rating display
   */
  createStarRating(rating, maxStars = 5, options = {}) {
    const { size = '1rem', className = '' } = options;
    const container = document.createElement('div');
    container.className = `tmyl-card__rating ${className}`;
    container.style.cssText = `
      display: flex;
      align-items: center;
      gap: 0.125rem;
    `;

    for (let i = 1; i <= maxStars; i++) {
      const star = this.create(i <= rating ? 'star' : 'starOutline', {
        size,
        color: i <= rating ? 'var(--tmyl-warning-500)' : 'var(--tmyl-neutral-300)'
      });
      container.appendChild(star);
    }

    return container;
  },

  /**
   * Create status badge with icon
   */
  createStatusBadge(status, options = {}) {
    const { size = '1rem', className = '' } = options;

    const statusConfig = {
      success: { icon: 'checkCircle', color: 'var(--tmyl-success-600)' },
      error: { icon: 'xCircle', color: 'var(--tmyl-error-600)' },
      warning: { icon: 'exclamationCircle', color: 'var(--tmyl-warning-600)' },
      info: { icon: 'infoCircle', color: 'var(--tmyl-info-600)' }
    };

    const config = statusConfig[status];
    if (!config) return null;

    return this.create(config.icon, {
      size,
      color: config.color,
      className: `tmyl-card__status-icon ${className}`
    });
  }
};

